import Foundation

public enum LoginError: Error, Equatable, LocalizedError {
    case clientError
    case generic(description: String)
    case invalidRequest
    case network
    case non200
    case userCancelled
    case serverError
    case accessDenied
    
    public var localizedDescription: String {
        switch self {
        case .clientError:
            return "client error"
        case .generic(description: let description):
            return description
        case .invalidRequest:
            return "invalid request"
        case .network:
            return "network"
        case .non200:
            return "non 200"
        case .userCancelled:
            return "user cancelled"
        case .serverError:
            return "server error"
        case .accessDenied:
            return "access denied"
        }
    }
    
    public var errorDescription: String? { return localizedDescription }
}
